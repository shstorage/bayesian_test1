# 원주방향 배관 두께 데이터 초기 및 마지막 값 생산 함수
def make_true(N_a, N_c, N_tn, delta_t, t_i_1, t_i_2, t_i_3):
    # y1 초기 데이터 생산
    y1 = np.linspace(0,2*np.pi,N_c)
    y1 = np.cos(y1)
    y1 = 0.1*y1 + 1   # 0.9 ~ 1.1 코사인함수 배관 원주방향 데이터 생성(중간값이 외호기준)    
    y1 = np.tile(y1.reshape(-1,1),N_a)
    y1 = np.tile(y1, (N_tn,1,1))
    
    # y3 감육진행 데이터 생산
    y3 = y1 - delta_t
    
    y2 = y1 - (t_i_2-t_i_1)/(t_i_3-t_i_1) * delta_t
        
    return y1, y2, y3


